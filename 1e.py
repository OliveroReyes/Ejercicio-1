numero = int(input("Ingresa un número del 1 al 7: "))


if numero == 1:
    print("lunes")
elif numero == 2:
    print("martes")
elif numero == 3:
    print("miércoles")
elif numero == 4:
    print("jueves")
elif numero == 5:
    print("viernes")
elif numero == 6:
    print("sabado")
elif numero == 7:
    print("domingo")
else:
    print("Error: El número debe estar en el rango del 1 al 7.")